package it.polimi.db2.progetto.e.views;

public class BestSellerOpView {

	int idOP;
	String nameOP;
	int numSell;
	
	public BestSellerOpView() {
		super();
	}

	public int getIdOP() {
		return idOP;
	}

	public void setIdOP(int idOP) {
		this.idOP = idOP;
	}

	public String getNameOP() {
		return nameOP;
	}

	public void setNameOP(String nameOP) {
		this.nameOP = nameOP;
	}

	public int getNumSell() {
		return numSell;
	}

	public void setNumSell(int numSell) {
		this.numSell = numSell;
	}
}